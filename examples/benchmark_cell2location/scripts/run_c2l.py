#!/usr/bin/env python3
"""Example skill-code implementation for the cell2location benchmark.

When used with test-skill --code, this file is executed in a namespace
containing ``input_dir``, ``output_csv``, and ``output_dir``. It must write a
proportions CSV with one row per spatial spot and one column per cell type.

In a real workflow this code would be generated by an Agent from SKILL.md.
"""

from pathlib import Path

import numpy as np
import pandas as pd
import scanpy as sc
from scipy.optimize import nnls

input_dir = Path(input_dir)  # type: ignore[name-defined]  # noqa: F821
output_csv = Path(output_csv)  # type: ignore[name-defined]  # noqa: F821

adata_sp = sc.read_h5ad(input_dir / "spatial.h5ad")
adata_ref = sc.read_h5ad(input_dir / "reference.h5ad")

shared_genes = list(adata_sp.var_names.intersection(adata_ref.var_names))
adata_sp = adata_sp[:, shared_genes].copy()
adata_ref = adata_ref[:, shared_genes].copy()

cell_types = adata_ref.obs["cell_type"].unique()
signatures = np.zeros((len(shared_genes), len(cell_types)), dtype=np.float32)
for idx, ctype in enumerate(cell_types):
    mask = adata_ref.obs["cell_type"] == ctype
    signatures[:, idx] = np.asarray(adata_ref[mask].X.mean(axis=0)).flatten()

sig_sums = signatures.sum(axis=0)
sig_sums[sig_sums == 0] = 1
signatures = signatures / sig_sums

proportions = np.zeros((adata_sp.n_obs, len(cell_types)), dtype=np.float32)
X = np.asarray(adata_sp.X.todense() if hasattr(adata_sp.X, "todense") else adata_sp.X)
for i in range(adata_sp.n_obs):
    target = X[i].astype(np.float64)
    if target.sum() == 0:
        proportions[i] = 1.0 / len(cell_types)
        continue
    target = target / target.sum()
    coef, _ = nnls(signatures, target)
    s = coef.sum()
    if s > 0:
        proportions[i] = coef / s
    else:
        proportions[i] = 1.0 / len(cell_types)

prop_df = pd.DataFrame(proportions, index=adata_sp.obs_names, columns=list(cell_types))
prop_df.to_csv(output_csv)
print(f"Wrote proportions to {output_csv}")
